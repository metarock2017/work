package org.redrock.play;

public class Wodi {
    private Integer id;
    private String wodiner;
    private Integer wodi;
    private Integer pid;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getWodiner() {
        return wodiner;
    }

    public void setWodiner(String wodiner) {
        this.wodiner = wodiner;
    }

    public Integer getWodi() {
        return wodi;
    }

    public void setWodi(Integer wodi) {
        this.wodi = wodi;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }
}
