package org.redrock.play;

public class Pingmin {
    private Integer id;
    private String pingminner;
    private Integer pingmin;
    private Integer pid;

    public Integer getPingmin() {
        return pingmin;
    }

    public void setPingmin(Integer pingmin) {
        this.pingmin = pingmin;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPingminner() {
        return pingminner;
    }

    public void setPingminner(String pingminner) {
        this.pingminner = pingminner;
    }

    public Integer getPid() {
        return pid;
    }

    public void setPid(Integer pid) {
        this.pid = pid;
    }
}
